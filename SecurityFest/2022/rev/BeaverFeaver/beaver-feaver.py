import re

parts = [
	("0A$", "1-0B"),
	("0A(.)-", "1-\\1B"),
	("^0B", "0C1-"),
	("(.)-0B", "\\1C1-"),
	("^0C", "0A1-"),
	("(.)-0C", "\\1A1-"),
	("1A$", "1-0H"),
	("1A(.)-", "1-\\1H"),
	("1B$", "2-0B"),
	("1B(.)-", "2-\\1B"),
	("1C$", "2-0C"),
	("1C(.)-", "2-\\1C"),
	("^2A", "0C2-"),
	("(.)-2A", "\\1C2-"),
	("^2B", "0B1-"),
	("(.)-2B", "\\1B1-"),
	("^2C", "0A2-"),
	("(.)-2C", "\\1A2-")
]

s = "0A"
n = 0
while True:
	for a, b in parts:
		s, c = re.subn(a, b, s)
		if c:
			n += c
			break
	else:
		break

print(f"SECFEST{{{n}}}")